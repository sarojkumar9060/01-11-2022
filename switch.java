public class switch {
    public static void main(String args[]){
        int day=7;
        switch (day) {
            case 1:
                System.out.println("sunday");
                break;
                case 2: 
                System.out.println("monday");
                break;
                case 3:
                System.out.println("tuesday");
                break;
                case 4:
                System.out.println("wednesday");
                break;
        
            default:
            System.out.println("plese enter valide number ");
                break;
        }
    }
}
